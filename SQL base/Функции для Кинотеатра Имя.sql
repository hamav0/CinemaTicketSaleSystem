-- Функция для ввода данных в таблицу "Сеансы"
CREATE PROCEDURE Ввод_Сеансы
    @Релиз_ИД INT,
    @Номер_Зала INT,
    @Дата_Время DATETIME
AS
BEGIN
    DECLARE @Продолжительность_Релиза INT;
    DECLARE @Время_Окончания_Предыдущего DATETIME;
    DECLARE @Время_Начала_Следующего DATETIME;

    -- Получаем продолжительность релиза
    SELECT @Продолжительность_Релиза = Хронометраж_Минут
    FROM База_Релизов.dbo.Релизы
    WHERE Релиз_ИД = @Релиз_ИД;

    IF @Продолжительность_Релиза IS NULL OR @Продолжительность_Релиза = 0
    BEGIN
        RAISERROR('Продолжительность релиза не найдена или равна 0.', 16, 1);
        RETURN;
    END;

    -- Находим время окончания ближайшего ПРЕДЫДУЩЕГО сеанса в этом зале
    SELECT TOP 1 @Время_Окончания_Предыдущего = DATEADD(minute, R.Хронометраж_Минут, S.Дата_Время)
    FROM Сеансы AS S
    JOIN База_Релизов.dbo.Релизы AS R ON S.Релиз_ИД = R.Релиз_ИД
    WHERE S.Номер_Зала = @Номер_Зала
      AND S.Дата_Время < @Дата_Время 
    ORDER BY S.Дата_Время DESC;

    -- Находим время начала ближайшего СЛЕДУЮЩЕГО сеанса в этом зале
    SELECT TOP 1 @Время_Начала_Следующего = S.Дата_Время
    FROM Сеансы AS S
    WHERE S.Номер_Зала = @Номер_Зала
      AND S.Дата_Время > @Дата_Время 
    ORDER BY S.Дата_Время ASC;

    -- 1. Проверяем наложение ПРЕДЫДУЩИМ (Новый должен начаться ПОСЛЕ конца старого)
    IF @Время_Окончания_Предыдущего IS NOT NULL
    BEGIN
        IF @Дата_Время < @Время_Окончания_Предыдущего
        BEGIN
        DECLARE @ErrorMessage NVARCHAR(4000);
        SET @ErrorMessage = N'Начало сеанса накладывается на время окончания предыдущего в этом зале. ' + CAST(@Дата_Время AS NVARCHAR(30));
        RAISERROR(@ErrorMessage, 16, 1);
            RETURN;
        END;
    END;

    -- 2. Проверяем наложение СО СЛЕДУЮЩИМ (Новый должен закончиться ДО начала следующего)
    IF @Время_Начала_Следующего IS NOT NULL
    BEGIN
        IF DATEADD(minute, @Продолжительность_Релиза, @Дата_Время) > @Время_Начала_Следующего
        BEGIN
            RAISERROR('Конец сеанса накладывается на начало следующего в этом зале.', 16, 1);
            RETURN;
        END;
    END;

    -- Если проверки пройдены, вставляем данные
    INSERT INTO Сеансы (Релиз_ИД, Номер_Зала, Дата_Время)
    VALUES (@Релиз_ИД, @Номер_Зала, @Дата_Время);
END;
GO

-- Функция для ввода данных в таблицу "Заказы"
CREATE PROCEDURE Ввод_Заказы
    @Сеанс_ИД INT,
    @Сумма_Оплаты MONEY = null
AS
BEGIN
    INSERT INTO Заказы (Сеанс_ИД, Сумма_Оплаты)
    VALUES (@Сеанс_ИД, @Сумма_Оплаты);
END;
GO

-- Функция для ввода данных в таблицу "Места"
CREATE PROCEDURE Ввод_Места
    @Место_ИД NVARCHAR(10) = NULL
AS
BEGIN
    DECLARE @Заказ_ИД INT;
    SELECT TOP 1 @Заказ_ИД = Заказ_ИД
    FROM Заказы
    ORDER BY Заказ_ИД DESC;
    INSERT INTO Места (Заказ_ИД, Место_ИД)
    VALUES (@Заказ_ИД, @Место_ИД);
END;
GO
--
-- Функция для ввода данных в таблицу "Сеансы_Форматы"
CREATE PROCEDURE Ввод_Форматы
    @Формат_ИД INT
AS
BEGIN
    DECLARE @Сеанс_ИД INT;
    SELECT TOP 1 @Сеанс_ИД = Сеанс_ИД
    FROM Сеансы
    ORDER BY Сеанс_ИД DESC;
    INSERT INTO Сеансы_Форматы (Сеанс_ИД, Формат_ИД)
    VALUES (@Сеанс_ИД, @Формат_ИД);
END;
GO